package org.apache.openejb.entity.cmp;

/**
 * @version $Revision$ $Date$
 */
public class SequenceTableKeyGenerator implements PrimaryKeyGenerator {
    private String tableName;
    private String sequenceName;
    private int batchSize;

    public SequenceTableKeyGenerator() {
    }

    public SequenceTableKeyGenerator(String tableName, String sequenceName, int batchSize) {
        this.tableName = tableName;
        this.sequenceName = sequenceName;
        this.batchSize = batchSize;
    }

    public String getTableName() {
        return tableName;
    }

    public void setTableName(String tableName) {
        this.tableName = tableName;
    }

    public String getSequenceName() {
        return sequenceName;
    }

    public void setSequenceName(String sequenceName) {
        this.sequenceName = sequenceName;
    }

    public int getBatchSize() {
        return batchSize;
    }

    public void setBatchSize(int batchSize) {
        this.batchSize = batchSize;
    }
}
